prog_languages=['Java','Python','C','C++','Swift','Perl']
print("The first three item in the list are: \n",prog_languages[0:3])     #starts from 0(first element) till 3rd element

print("The three middle item of the list are: \n",prog_languages[2:5])    #starts from 2(2+1 element) till 5th element

print("The last three items of the list are: \n",prog_languages[3:6])      #starts from 3(3 +1 element till 6th element